// Add the ten last posts of af categroy menu item in a sub menu
function display_posts_for_categories_menu_item( $items, $menu, $args ) {


	$menu_order = count($items); /* Offset menu order */
	$child_items = array();

	// Loop through the menu items looking category menu object
	foreach ( $items as $item ) {
		// echo $item->object_id . ':' . $item->title . ';';
		// 只显示以下目录的文章列表
		if ($item->object_id == 41 || $item->object_id == 37 ||
			$item->object_id == 42 || $item->object_id == 38 ||
			$item->object_id == 43 || $item->object_id == 39 ||
			$item->object_id == 44 || $item->object_id == 36 ||
			$item->object_id == 45 || $item->object_id == 40 ||
			$item->object_id == 33 || $item->object_id == 32 ||
      $item->object_id == 21 || $item->object_id == 22
			) {
			    // Query the category posts
			    $arguments = array(
			            'numberposts' => -1,
			            'cat' => $item->object_id,
				    	// 'category_name' => $item->title,
			            'orderby' => 'date',
			            'order' => 'DESC'
			    );

			    foreach ( get_posts( $arguments ) as $post ) {
			    	// echo $post->post_title;
			    	// echo $item->ID;
			        // Add sub menu item
			        $post->menu_item_parent = $item->ID;
			        $post->post_type = 'nav_menu_item';
			        $post->object = 'custom';
			        $post->type = 'custom';
			        $post->menu_order = ++$menu_order;
			        $post->title = $post->post_title;
			        $post->url = get_permalink( $post->ID );
			        /* add children */
			        $child_items[] = $post;
			    }
		}
	}
	return array_merge( $items, $child_items );
}
